import math
import matplotlib.pyplot as plt

# 1. Dados do problema
C = 500.00      # Capital inicial
M = 1500.00     # Montante desejado
i = 0.0025      # Taxa de juros (0,25% em decimal)

# 2. Aplicando a exata fórmula que você deduziu: t = log(M / C) / log(1 + i)
t = math.log(M / C) / math.log(1 + i)

print("-" * 40)
print(f"Resultado do Cálculo:")
print(f"Tempo exato: {t:.2f} meses")
print(f"Em anos: {t / 12:.2f} anos")
print("-" * 40)

# 3. Gerando os dados para o Gráfico
# Criamos uma lista de meses de 0 até o mês final calculado (arredondado para cima)
meses_lista = list(range(int(t) + 2))

# Calculamos o montante para cada mês da lista
evolucao_montante = [C * ((1 + i) ** mes) for mes in meses_lista]

# 4. Configurando e exibindo o Gráfico
plt.figure(figsize=(10, 6))
plt.plot(meses_lista, evolucao_montante, color='blue', linewidth=2, label='Crescimento do Capital')

# Linhas de marcação para o alvo
plt.axhline(y=M, color='red', linestyle='--', label=f'Montante Alvo (R$ {M:.2f})')
plt.axvline(x=t, color='green', linestyle='--', label=f'Tempo Necessário ({t:.0f} meses)')

# Títulos e legendas
plt.title('Evolução do Montante - Juros Compostos (0,25% a.m.)')
plt.xlabel('Tempo (Meses)')
plt.ylabel('Montante (R$)')
plt.legend()
plt.grid(True)

# Mostra o gráfico na tela
plt.show()

