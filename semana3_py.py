### Aplicações práticas para a função log(.)

import math

def calcular_tempo_juros_compostos(montante, capital, taxa_juros_percentual):
    """
    Calcula o tempo necessário para um capital atingir um montante
    específico sob regime de juros compostos.
    """
    # Converte a taxa de juros de porcentagem para decimal (ex: 0.25% vira 0.0025)
    i = taxa_juros_percentual / 100

    # Aplica a fórmula derivada com logaritmos
    # math.log() calcula o logaritmo natural (base e), mas qualquer base funcionaria aqui
    tempo = math.log(montante / capital) / math.log(1 + i)

    return tempo

