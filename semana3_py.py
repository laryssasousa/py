multiplicador = 2  # Queremos descobrir quando a população dobra (Pt = 2 * P0)

# População do ano base (P0).
# Escolhi 1000 como exemplo para podermos visualizar no gráfico.
P0 = 1000

# 2. A fórmula exata isolando o tempo (t) com logaritmos
t = math.log(multiplicador) / math.log(1 + taxa)

# Exibindo o resultado no terminal
print("-" * 40)
print("CÁLCULO DE CRESCIMENTO POPULACIONAL")
print("-" * 40)
print(f"Taxa de crescimento: {taxa * 100}% ao ano")
print(f"Tempo exato para DOBRAR a população: {t:.2f} anos")
print("-" * 40)

# 3. Gerando os dados para o Gráfico
# Cria uma lista de anos do ano 0 até o ano final calculado
anos_lista = list(range(int(t) + 2))

# Calcula a população para cada ano usando a fórmula: Pt = P0 * (1.03)^t
evolucao_populacao = [P0 * ((1 + taxa) ** ano) for ano in anos_lista]

# 4. Configurando e exibindo o Gráfico
plt.figure(figsize=(10, 6))
plt.plot(anos_lista, evolucao_populacao, color='purple', linewidth=2, label='Curva de Crescimento')

